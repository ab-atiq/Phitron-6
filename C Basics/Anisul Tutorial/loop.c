#include<stdio.h>
int main()
{
    int i,j,k;
    for(i=0; i<10; i++)
    {
        printf("My name is Atiqur Rahman.\n");
    }

    // while loop use here
    j=1;
    while (j<=10)
    {
        printf("Abul Bashar Atiqur.\n");
        j++;
    }

    //do-while loop use here
    k=5;
    do
    {
        printf("Atiqur Rahman.\n");
        k++;
    }
    while (k<=10);

    return 0;

}
